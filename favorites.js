function renderFavorites() {
  const root = document.getElementById("favorites-root");
  if (!root) return;

  root.innerHTML = favoritesData.map(section => {
    if (section.type === "cards") {
      return `
        <button class="toggle-btn" onclick="toggleSection('${section.id}')">${section.title}</button>
        <div id="${section.id}" class="toggle-content">
          <div class="games-grid">
            ${section.items.map(item => `
              <div class="game-card" data-title="${item.title}">
                <img src="${item.img}" alt="${item.title}">
              </div>
            `).join("")}
          </div>
        </div>
      `;
    }

    return `
      <button class="toggle-btn" onclick="toggleSection('${section.id}')">${section.title}</button>
      <div id="${section.id}" class="toggle-content">
        <div class="text-block">
          <ul class="love-list">
            ${section.items.map(item => `<li>${item.title}</li>`).join("")}
          </ul>
        </div>
      </div>
    `;
  }).join("");

  bindGameCards();
}

function bindGameCards() {
  const gameCards = document.querySelectorAll(".game-card");
  const gameModal = document.getElementById("game-modal");
  const gameModalImg = document.getElementById("game-modal-img");
  const gameModalTitle = document.getElementById("game-modal-title");

  gameCards.forEach(card => {
    card.addEventListener("click", () => {
      const img = card.querySelector("img");
      gameModalImg.src = img.src;
      gameModalImg.alt = img.alt;
      gameModalTitle.textContent = card.dataset.title;
      gameModal.classList.remove("hidden");
      document.body.style.overflow = "hidden";
    });
  });
}

document.addEventListener("DOMContentLoaded", renderFavorites);
